const _throttle = () => {
    let last = 0,
        timer = null

    return async function(fn, delay = 3000) {
        let context = this
        let args = arguments
        let now = +new Date()

        if (now - last < delay) {
            clearTimeout(timer)
            timer = setTimeout(async function() {
                last = now
                await fn.apply(context, args)
            }, delay)
        } else {
            last = now
            await fn.apply(context, args)
        }
    }
}
